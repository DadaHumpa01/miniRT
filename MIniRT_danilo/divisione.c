#include <stdio.h>

int main(void)
{
    int i;
    int j;

    i = 0;
    j = 0;
    while (i != 20)
    {
        j = 0;
        while (j != 20)
        {
            printf("%f, %f\n",i / 20., j / 20.);
            j++;
        }
        i++;
    }
}